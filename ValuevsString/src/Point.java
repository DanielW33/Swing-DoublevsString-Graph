public class Point {
    private double x;
    private double y;
    private double Value;
    private String str;

    public double getX(){
        return x;
    }
    public void setX(double x){
        this.x = x;
    }
    public double getY(){
        return y;
    }
    public void setY(double y){
        this.y = y;
    }
    public double getValue(){
        return Value;
    }
    public void setValue(double value){
        this.Value = value;
    }
    public String getString(){
        return str;
    }
    public void setString(String string){
        this.str = string;
    }
}
